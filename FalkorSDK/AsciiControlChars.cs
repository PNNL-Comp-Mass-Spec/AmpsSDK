namespace FalkorSDK
{
    /// <summary>
    /// The ascii control chars.
    /// </summary>
    public enum AsciiControlChars
    {
        /// <summary>
        /// Null.
        /// </summary>
        NUL = 0, 

        /// <summary>
        /// Start of Heading.
        /// </summary>
        SOH, 

        /// <summary>
        /// Start of Text.
        /// </summary>
        STX, 

        /// <summary>
        /// End of Text.
        /// </summary>
        ETX, 

        /// <summary>
        /// End of Transmission.
        /// </summary>
        EOT, 

        /// <summary>
        /// Enquiry.
        /// </summary>
        ENQ, 

        /// <summary>
        /// Acknowledge.
        /// </summary>
        ACK, 

        /// <summary>
        /// Bell.
        /// </summary>
        BEL, 

        /// <summary>
        /// Backspace.
        /// </summary>
        BS, 

        /// <summary>
        /// Horizontal tab.
        /// </summary>
        HT, 

        /// <summary>
        /// Line Feed.
        /// </summary>
        LF, 

        /// <summary>
        /// Vertical Tab.
        /// </summary>
        VT, 

        /// <summary>
        /// Form Feed.
        /// </summary>
        FF, 

        /// <summary>
        /// Cariage return.
        /// </summary>
        CR, 

        /// <summary>
        /// Shift out.
        /// </summary>
        SO, 

        /// <summary>
        /// Shift in.
        /// </summary>
        SI, 

        /// <summary>
        /// Data link escape.
        /// </summary>
        DLE, 

        /// <summary>
        /// Device Control 1.
        /// </summary>
        DC1, 

        /// <summary>
        /// Device Control 2.
        /// </summary>
        DC2, 

        /// <summary>
        /// Device Control 3.
        /// </summary>
        DC3, 

        /// <summary>
        /// Device Control 4.
        /// </summary>
        DC4, 

        /// <summary>
        /// Negative acknowledge
        /// </summary>
        NAK, 

        /// <summary>
        /// Synchronous idle.
        /// </summary>
        SYN, 

        /// <summary>
        /// End of Transmission block
        /// </summary>
        ETB, 

        /// <summary>
        /// Cancle.
        /// </summary>
        CAN, 

        /// <summary>
        /// End of Medium.
        /// </summary>
        EM, 

        /// <summary>
        /// Substitute
        /// </summary>
        SUB, 

        /// <summary>
        /// Escape.
        /// </summary>
        ESC, 

        /// <summary>
        /// File separator
        /// </summary>
        FS, 

        /// <summary>
        /// Group separator
        /// </summary>
        GS, 

        /// <summary>
        /// Record separator
        /// </summary>
        RS, 

        /// <summary>
        /// Unit separator
        /// </summary>
        US, 
    }
}